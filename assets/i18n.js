window.VP_I18N = (() => {
  const LS = "vp_lang";
  function getLang() { return localStorage.getItem(LS) || document.documentElement.dataset.defaultLang || "ru"; }
  function setLang(lang) {
    if (!window.VP_DICT[lang]) return;
    localStorage.setItem(LS, lang);
    document.documentElement.setAttribute("lang", lang === "kz" ? "kk" : lang);
    const d = window.VP_DICT[lang];
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      if (d[key] != null) el.textContent = d[key];
    });
    document.querySelectorAll("[data-i18n-ph]").forEach(el => {
      const key = el.getAttribute("data-i18n-ph");
      if (d[key] != null) el.setAttribute("placeholder", d[key]);
    });
    document.querySelectorAll("[data-i18n-attr]").forEach(el => {
      // syntax: "title:key, aria-label:other.key"
      const spec = el.getAttribute("data-i18n-attr");
      spec.split(",").forEach(pair => {
        const [attr, key] = pair.trim().split(":");
        if (d[key] != null) el.setAttribute(attr, d[key]);
      });
    });
    document.querySelectorAll(".vp-lang button").forEach(b => {
      b.classList.toggle("is-active", b.dataset.lang === lang);
    });
  }
  function init() {
    document.querySelectorAll(".vp-lang button").forEach(b => {
      b.addEventListener("click", () => setLang(b.dataset.lang));
    });
    setLang(getLang());
  }
  return { getLang, setLang, init };
})();

document.addEventListener("DOMContentLoaded", () => {
  document.documentElement.classList.add("js-ready");
  window.VP_I18N.init();

  // burger
  const burger = document.querySelector(".vp-burger");
  const drawer = document.querySelector(".vp-drawer");
  if (burger && drawer) {
    burger.addEventListener("click", () => drawer.classList.toggle("is-open"));
    drawer.querySelectorAll("a").forEach(a => a.addEventListener("click", () => drawer.classList.remove("is-open")));
  }

  // reveal on scroll
  const obs = new IntersectionObserver((entries) => {
    entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add("is-visible"); obs.unobserve(e.target); } });
  }, { threshold: 0.12 });
  document.querySelectorAll(".vp-reveal").forEach(el => obs.observe(el));

  // whatsapp links
  const WA_PHONE = "77001234567"; // placeholder KZ number
  document.querySelectorAll("[data-whatsapp]").forEach(a => {
    a.href = `https://wa.me/${WA_PHONE}?text=${encodeURIComponent("Здравствуйте! Хочу оформить заказ в Vkusno Polezno.")}`;
    a.target = "_blank"; a.rel = "noopener";
  });
});
